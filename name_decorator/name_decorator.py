# David Garcia Name decorator Py
name = input("What is your name?:\n")

print(f"<<{name}>>")
print(f"##{name}##")
print(f"\\{name}//")
print(f"$${name}$$")
print(f"%%{name}%%")
print(f"@@{name}@@")
print(f"^^{name}^^")

print("Thanks for using this program to make your name a little bit silly by decorating it, I hope yu had fun!!!")
